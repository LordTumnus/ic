function T = generateTestData()
    rng(7);
    n = 20;

    tests = [ ...
        "TC_MotCtrl_Torque_Limit", "TC_MotCtrl_Ramp_Rate", "TC_MotCtrl_Overcurrent", ...
        "TC_SensFus_Kalman_Init", "TC_SensFus_GPS_Timeout", "TC_SensFus_IMU_Drift", ...
        "TC_Diag_DTC_Storage", "TC_Diag_Snapshot_Capture", "TC_Diag_Clear_All", ...
        "TC_CAN_Msg_Timeout", "TC_CAN_Bus_Off_Recovery", "TC_CAN_DLC_Validation", ...
        "TC_PwrMgmt_Sleep_Entry", "TC_PwrMgmt_Wakeup_Source", "TC_PwrMgmt_Undervoltage", ...
        "TC_Safety_Watchdog_Trip", "TC_Safety_ASIL_Decomp", "TC_Safety_E2E_CRC", ...
        "TC_HIL_Regression_Full", "TC_HIL_Timing_Jitter"]';

    modules = [ ...
        "Motor Control", "Motor Control", "Motor Control", ...
        "Sensor Fusion", "Sensor Fusion", "Sensor Fusion", ...
        "Diagnostics", "Diagnostics", "Diagnostics", ...
        "CAN Stack", "CAN Stack", "CAN Stack", ...
        "Power Mgmt", "Power Mgmt", "Power Mgmt", ...
        "Safety", "Safety", "Safety", ...
        "HIL", "HIL"]';

    levels = [ ...
        "SWE.4","SWE.4","SWE.4", ...
        "SWE.4","SWE.4","SWE.4", ...
        "SWE.4","SWE.4","SWE.4", ...
        "SWE.5","SWE.5","SWE.5", ...
        "SWE.5","SWE.5","SWE.5", ...
        "SWE.6","SWE.6","SWE.6", ...
        "SWE.6","SWE.6"]';

    results = ["Pass","Fail","Blocked","Skip"];
    weights = [0.70, 0.12, 0.10, 0.08];
    cumW = cumsum(weights);
    Result = strings(n, 1);
    for i = 1:n
        r = rand();
        Result(i) = results(find(r <= cumW, 1));
    end
    Result(1) = "Pass"; Result(4) = "Pass"; Result(10) = "Fail";
    Result(16) = "Pass"; Result(19) = "Blocked";

    TestCase = tests;
    Level = levels;
    Module = modules;

    Priority = strings(n, 1);
    for i = 1:n
        if contains(Module(i), "Safety") || Level(i) == "SWE.6"
            Priority(i) = "ASIL-B";
        else
            Priority(i) = "QM";
        end
    end

    Coverage = randi([45 98], n, 1);
    Coverage(Result == "Pass") = randi([75 98], sum(Result == "Pass"), 1);
    Coverage(Result == "Fail") = randi([30 70], sum(Result == "Fail"), 1);

    Duration = cell(n, 1);
    for i = 1:n
        base = 0.5 + 4*rand();
        Duration{i} = base + 0.3*randn(1, 5);
    end

    LastRun = datetime(2026, 4, 1) + days(randi(11, n, 1));

    T = table(Result, TestCase, Level, Module, Priority, Coverage, Duration, LastRun);
end

% -- report tab (figures grid + Typst document) ----------------------------

function buildReportTab(panel, plotFiles)
    row = ic.FlexContainer("Direction", "row", "Gap", 0, "Padding", 0);
    panel.addChild(row);
    row.css.fill();

    % left: 6 test-related figures in a 3x2 grid
    figGrid = ic.GridContainer( ...
        "Columns", "1fr 1fr", "Rows", "1fr 1fr 1fr", "Gap", 8);
    row.addChild(figGrid);
    figGrid.css.style("", "flex", "1", "padding", "12px", "minWidth", "0");
    for i = 1:6
        img = ic.Image("Source", plotFiles(i), "ObjectFit", "contain");
        figGrid.addChild(img);
        img.css.style("", ...
            "borderRadius", "6px", ...
            "border", "1px solid rgba(255,255,255,0.06)", ...
            "backgroundColor", "rgba(255,255,255,0.02)");
    end

    % right: Typst test report
    typstDoc = ic.Typst( ...
        "Value", buildTypstSource(plotFiles(1), plotFiles(5)), ...
        "ToolbarOnHover", true);
    row.addChild(typstDoc);
    typstDoc.css.style("", "flex", "1", "minWidth", "0");
end

function files = generateAllPlots()
    outDir = fullfile(tempdir, "hero_plots");
    if ~isfolder(outDir), mkdir(outDir); end

    darkBg = [0.06 0.06 0.08];
    axBg   = [0.08 0.09 0.12];
    gridC  = [0.2 0.2 0.25];
    axC    = [0.45 0.45 0.5];
    titleC = [0.88 0.88 0.92];

    rng(42);
    files = strings(1, 6);

    % create all 6 figures first
    figs = gobjects(1, 6);
    for idx = 1:6
        f = figure("Visible", "off", "Color", darkBg, ...
            "Position", [100 100 520 340]);
        ax = axes(f, "Color", axBg, "XColor", axC, "YColor", axC, ...
            "GridColor", gridC, "GridAlpha", 0.4, "Box", "off");
        hold(ax, "on");
        grid(ax, "on");

        switch idx
            case 1  % coverage trend over sprints
                sprints = 1:10;
                stmt = [62 65 68 71 74 76 78 80 81 82];
                branch = [48 51 55 58 60 63 65 68 70 72];
                mcdc = [30 33 37 40 44 48 51 55 58 61];
                plot(ax, sprints, stmt,   "-o", "Color", [0.39 0.45 0.96], "LineWidth", 2, "MarkerSize", 4);
                plot(ax, sprints, branch, "-s", "Color", [0.13 0.77 0.37], "LineWidth", 2, "MarkerSize", 4);
                plot(ax, sprints, mcdc,   "-^", "Color", [0.97 0.62 0.17], "LineWidth", 2, "MarkerSize", 4);
                yline(ax, 80, "--", "Color", [0.9 0.3 0.3], "LineWidth", 1, "Alpha", 0.6);
                legend(ax, "Statement", "Branch", "MC/DC", "Target", ...
                    "TextColor", [0.7 0.7 0.75], "Color", axBg, "EdgeColor", gridC);
                title(ax, "Coverage Trend (per Sprint)", "Color", titleC);
                xlabel(ax, "Sprint"); ylabel(ax, "%");
                ylim(ax, [20 100]);

            case 2  % pass/fail/blocked per module (stacked bar)
                mods = categorical(["MotCtrl","SensFus","Diag","CAN","PwrMgmt","Safety","HIL"]);
                mods = reordercats(mods, ["MotCtrl","SensFus","Diag","CAN","PwrMgmt","Safety","HIL"]);
                data = [8 6 7 5 4 6 3;   % pass
                        1 2 1 2 1 0 1;   % fail
                        0 1 0 1 1 1 2;   % blocked
                        1 0 1 0 0 0 0]'; % skip
                b = bar(ax, mods, data, "stacked");
                b(1).FaceColor = [0.13 0.77 0.37];
                b(2).FaceColor = [0.94 0.27 0.27];
                b(3).FaceColor = [0.97 0.62 0.17];
                b(4).FaceColor = [0.45 0.45 0.5];
                legend(ax, "Pass", "Fail", "Blocked", "Skip", ...
                    "TextColor", [0.7 0.7 0.75], "Color", axBg, "EdgeColor", gridC);
                title(ax, "Results by Module", "Color", titleC);
                ylabel(ax, "Test Cases");

            case 3  % scatter: coverage vs execution time
                cov = 40 + 55*rand(50, 1);
                dur = 0.5 + 5*rand(50, 1);
                colors = zeros(50, 3);
                for k = 1:50
                    if cov(k) >= 80
                        colors(k,:) = [0.13 0.77 0.37];
                    elseif cov(k) >= 60
                        colors(k,:) = [0.97 0.62 0.17];
                    else
                        colors(k,:) = [0.94 0.27 0.27];
                    end
                end
                scatter(ax, cov, dur, 35, colors, "filled", "MarkerFaceAlpha", 0.7);
                title(ax, "Coverage vs Duration", "Color", titleC);
                xlabel(ax, "Coverage (%)"); ylabel(ax, "Duration (s)");

            case 4  % area: defects open/closed over time
                weeks = 1:12;
                opened = [5 8 4 6 3 7 5 4 2 3 4 2];
                closed = [2 5 6 5 4 6 7 5 3 4 5 3];
                area(ax, weeks, opened, "FaceColor", [0.94 0.27 0.27], ...
                    "FaceAlpha", 0.3, "EdgeColor", [0.94 0.27 0.27], "LineWidth", 1.5);
                area(ax, weeks, closed, "FaceColor", [0.13 0.77 0.37], ...
                    "FaceAlpha", 0.3, "EdgeColor", [0.13 0.77 0.37], "LineWidth", 1.5);
                legend(ax, "Opened", "Closed", ...
                    "TextColor", [0.7 0.7 0.75], "Color", axBg, "EdgeColor", gridC);
                title(ax, "Defect Trend (12 weeks)", "Color", titleC);
                xlabel(ax, "Week"); ylabel(ax, "Count");

            case 5  % bar: requirement traceability
                cats = categorical(["SWE.4", "SWE.5", "SWE.6"]);
                cats = reordercats(cats, ["SWE.4", "SWE.5", "SWE.6"]);
                traced = [142 68 34];
                total  = [165 82 45];
                b = bar(ax, cats, [traced; total-traced]', "stacked");
                b(1).FaceColor = [0.39 0.45 0.96];
                b(2).FaceColor = [0.25 0.25 0.3];
                legend(ax, "Traced", "Untraceable", ...
                    "TextColor", [0.7 0.7 0.75], "Color", axBg, "EdgeColor", gridC);
                title(ax, "Requirement Traceability", "Color", titleC);
                ylabel(ax, "Requirements");

            case 6  % histogram: test execution times
                times = 0.5 - 2*log(rand(300, 1));
                histogram(ax, times, 25, ...
                    "FaceColor", [0.51 0.36 0.84], "EdgeColor", "none", ...
                    "FaceAlpha", 0.7);
                xline(ax, 5, "--", "Color", [0.9 0.3 0.3], "LineWidth", 1.5, "Alpha", 0.7);
                title(ax, "Execution Time Distribution", "Color", titleC);
                xlabel(ax, "Duration (s)"); ylabel(ax, "Count");
        end
        figs(idx) = f;
    end

    % batch export all at once, then close
    for idx = 1:6
        fname = fullfile(outDir, sprintf("plot_%d.png", idx));
        print(figs(idx), fname, "-dpng", "-r150");
        files(idx) = fname;
    end
    close(figs);
end

