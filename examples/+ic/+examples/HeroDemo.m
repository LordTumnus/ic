% -- callbacks -------------------------------------------------------------

function wireCallbacks(header, tc, tbl, drawer, frame, sidebar)
    addlistener(header.menuBtn, 'Clicked', @(~,~) sidebar.drawer.open());
    addlistener(tbl, 'SelectionChanged', @(~,e) onRowSelected(e, tbl, drawer));

    tabs = {tc.overviewTab, tc.reportTab, tc.archTab};
    tabIds = cellfun(@(t) t.ID, tabs, 'UniformOutput', false);
    allBtns = sidebar.navBtns;

    % nav buttons -> select tab + highlight
    for i = 1:numel(tabs)
        addlistener(allBtns{i}, 'Clicked', ...
            @(~,~) onNavClicked(tc.container, tabs{i}, sidebar.drawer, allBtns, i));
    end

    % tab changed (by click or programmatic) -> sync nav highlight + toast
    addlistener(tc.container, 'ValueChanged', ...
        @(~,e) onTabChanged(e, frame, allBtns, tabIds));
end

function onNavClicked(tabContainer, tab, drawer, allBtns, idx)
    tabContainer.selectTab(tab);
    drawer.close();
    highlightNav(allBtns, idx);
end

function highlightNav(allBtns, activeIdx)
    for j = 1:numel(allBtns)
        allBtns{j}.css.clearStyle("");
        allBtns{j}.css.style("", "justifyContent", "flex-start", "width", "100%");
        if j == activeIdx
            allBtns{j}.css.style("", ...
                "backgroundColor", "rgba(99,102,241,0.12)", ...
                "color", "#818cf8");
        end
    end
end

function onRowSelected(e, tbl, d)
    sel = e.Data.selection;
    if ~strcmp(sel.type, 'row'), return; end
    if isempty(sel.value), return; end
    rowIdx = sel.value(1);
    if rowIdx < 1 || rowIdx > height(tbl.Data), return; end
    row = tbl.Data(rowIdx, :);
    fprintf("Row %d: %s [%s] %s\n", rowIdx, row.TestCase, row.Result, row.Module);
    d.taskLabel.Text = string(row.TestCase);
    d.vals{1}.Text = string(row.Result);
    d.vals{2}.Text = string(row.Level);
    d.vals{3}.Text = string(row.Module);
    d.vals{4}.Text = string(row.Priority);
    d.vals{5}.Text = string(row.LastRun);
    d.bar.Value = row.Coverage;
    d.drawer.open();
end

function onTabChanged(e, frame, allBtns, tabIds)
    val = string(e.Data.value);
    if val == "", return; end

    % sync nav highlight to whichever tab was selected
    for k = 1:numel(tabIds)
        if val == tabIds{k}
            highlightNav(allBtns, k);
            break;
        end
    end

    frame.addChild(ic.Toast( ...
        "Value", "Switched tab", ...
        "Variant", "info", ...
        "Duration", 2, ...
        "Position", "top"));
end

